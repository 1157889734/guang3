#define UART1_FIFO_SIZE			(512)		//缓冲区大小定义
#define UART1_IRQ_SLOT			EIC_SIR10	//中断控制器优先级的槽
#define UART1_IRQ_VECTOR		(10)		//中断向量号
#define UART1_IRQ_PRIORITY		(11)		//中断优先级---最高15



//&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&& 接口函数 &&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&&

//***********************************************************************************************************************
//函数作用:UART1中断服务程序
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void UART1_IRQHandler(void);

//***********************************************************************************************************************
//函数作用:UART1初始化
//参数说明:波特率
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void  uart1_init(uint32 bps);

//***********************************************************************************************************************
//函数作用:从软件接收FIFO读取单个字符功能（非阻塞模式）
//参数说明:ch---读取字符存放地址
//注意事项:
//返回说明:如果有数据返回1
//***********************************************************************************************************************
extern uint8 uart1_read_char(uint8 *ch);

//***********************************************************************************************************************
//函数作用:向软件发送FIFO写入一个字节并启动发送（非阻塞模式）
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void uart1_write_char(uint8 ch);

//***********************************************************************************************************************
//函数作用:向软件发送FIFO写入一定长度的数据并启动发送（非阻塞模式）
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern uint8 uart1_write_buf(uint8 *buf, uint16 length);

//***********************************************************************************************************************
//函数作用:向软件发送FIFO写入字符串并启动发送（非阻塞模式）
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void uart1_write_str(char *str);

//***********************************************************************************************************************
//函数作用:备份软件接收FIFO的读指针
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void uart1_fifo_out_backup(void);

//***********************************************************************************************************************
//函数作用:恢复软件接收FIFO的读指针
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern void uart1_fifo_out_resume(void);

//***********************************************************************************************************************
//函数作用:获取硬件测试命令
//参数说明:
//注意事项:
//返回说明:无
//***********************************************************************************************************************
extern uint8 uart1_get_hardware_test(void);



#endif


